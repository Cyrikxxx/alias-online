async function main() {
  console.log("Seeding...");
  let totalWords = 0;

  for (const cat of categories) {
    const category = await prisma.category.upsert({
      where: { slug: cat.slug },
      update: { emoji: cat.emoji, name: cat.name },
      create: { name: cat.name, slug: cat.slug, emoji: cat.emoji },
    });

    for (const text of cat.words) {
      const word = await prisma.word.upsert({
        where: { text },
        update: {},
        create: { text, difficulty: 1 },
      });

      await prisma.wordCategory.upsert({
        where: { wordId_categoryId: { wordId: word.id, categoryId: category.id } },
        update: {},
        create: { wordId: word.id, categoryId: category.id },
      });
    }

    totalWords += cat.words.length;
    console.log(`  ${cat.emoji} ${cat.name}: ${cat.words.length} слов`);
  }

  console.log(`Done. ${categories.length} категорий, ${totalWords} слов.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
